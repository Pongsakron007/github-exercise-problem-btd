'''
คำอธิบาย

ให้หาด้านตรงข้ามมุมฉาก โดยใช้สูตร
a กำลังสอง + b กำลังสอง = c กำลังสอง
หรือ (a * a) + (b * b) = (c * c)

โดย a , b = ด้านประกอบมุมฉาก
c = ด้านตรงข้ามมุมฉาก


รูปเเบบ Input
รับจำนวนเต็มสองตัว เช่น
3
4

รูปเเบบ Output
ออกมาเป็นจำนวนเต็ม หนึ่งจำนวน เช่น

5

'''

import math

x, y = [int(input()) for i in range(2)]

total = x**2 + y**2

z = int(math.sqrt(total))

print(z)
